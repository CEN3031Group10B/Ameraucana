'use strict';

angular.module('items').factory('Items', ['$http',
  function($http) {
    var methods = {
      getAllItems: function() {
        return $http.get('/api/items');
      },

      read: function(username) {
        return $http.get('/api/items', username);
      },

      create: function(item) {
        return $http.post('/api/items', item);
      },

      update: function(name, item) {
        return $http.put('/api/items', name, item);
      }
    };

    return methods;
  }
]);
